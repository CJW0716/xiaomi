// 找到折叠导航条
var collpaseNav = document.getElementById('collapse-nav')
// 找到所有的li标签
var lis = document.querySelectorAll('#two>ul>li')
// 遍历每一个li标签
lis.forEach(function(ele, i, array) {
	ele.onmouseenter = function(event) {
		
		// 清空二级导航栏内容
		collpaseNav.innerHTML = ''
		// 根据索引找到索引对应的数据
		var data = arr[i]
		// 创建ul标签
		var ul = document.createElement('ul')
		// 循环遍历数据.content
		for (var idx in data.content) {
			// 创建li标签
			var li = document.createElement('li')
			var span1 = document.createElement('span')
			var img = document.createElement('img')
			var span2 = document.createElement('span')
			var span3 = document.createElement('span')

			span2.innerText = data.content[idx].name
			span3.innerText = data.content[idx].price
			span3.classList = 'og'
			if ((data.content[idx].xin) != '') {
				li.innerHTML = "<span class='xin'>" + data.content[idx].xin + "</span><img src = '" + data.content[idx].src +
					"' alt=''/>"
			} else {
				li.innerHTML = "<span >" + '' + "</span><img src = '" + data.content[idx].src + "' alt=''/>"
			}
			li.appendChild(span2)
			if ((data.content[idx].price) != '') {
				li.appendChild(span3)
			} else {
				li.appendChild(span3)
			}

			ul.appendChild(li)

		}
		// ul 添加到二级导航中
		collpaseNav.appendChild(ul)
		collpaseNav.classList.add('show')
	}
	// 隐藏二级导航
	ele.onmouseleave = function() {
		collpaseNav.classList.remove('show')
	}

})
collpaseNav.onmouseenter = function() {
	collpaseNav.classList.add('show')
}
collpaseNav.onmouseleave = function() {
	collpaseNav.classList.remove('show')
}
// 侧边导航栏
// 记录当前选中数据占用的宽度
var w = 0
$('#three>ul>li').hover(function(event) {
	$(this).addClass('actives')
	$('#content').html('')

	// 根据移动到li的index索引值,取出对应的数据
	var data = arr1[$(this).attr('index')].data
	for (var idx in data) {
		$('<section>')
			.attr('class', 'item')
			.appendTo($('#content'))
			// .append(`<img src=${data[idx].src}>`)
			.append($('<img>').attr('src', data[idx].src))
			.append($('<span>').text(data[idx].name))
	}
	w = Math.ceil(data.length / 6) * 260
	$('#content').width(Math.ceil(data.length / 6) * 260)
}, function() {
	$(this).removeClass('actives')
	$('#content').width(0)
})

$('#content').hover(function() {
	$('#content').width(w)
}, function() {
	$('#content').width(0)
})
